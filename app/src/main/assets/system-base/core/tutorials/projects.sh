    UNDERSTANDING PROJECTS
════════════════════════════════════════

In Studio OS, everything you create is
stored as a "project" — a folder with
your files inside.

WHERE PROJECTS ARE STORED
──────────────────────────
  /data/data/com.studio.os/files/projects/

Each project has its own subfolder, e.g.:
  projects/my-website/
  projects/my-script/
  projects/my-ai-bot/

OPENING A PROJECT
─────────────────
1. Home → My Projects (option 2)
2. Pick your project by number
3. Choose what to do with it

WHAT YOU CAN DO WITH A PROJECT
───────────────────────────────
• Edit files (opens editor)
• Run project (executes main script)
• View files (file manager)
• Delete project

LAST BUILD
──────────
Studio OS remembers your last project.
Use 'Continue my last build' (option 4)
to quickly jump back to it.

BACKING UP PROJECTS
───────────────────
• Use the terminal to copy files
• Or use Android's file manager to access
  your app's files (if allowed by Android)

