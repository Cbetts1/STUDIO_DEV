# Tutorial: How to create a website

cat <<'TUT'
════════════════════════════════════════
    HOW TO CREATE A WEBSITE
════════════════════════════════════════

1. From Home, press '1' — Create something new
2. Press '1' — Website / Web App
3. Follow the wizard:
   • Enter a project name (e.g. my-site)
   • Choose website type
   • Enter a title

4. Studio OS will create:
   • index.html  — your main page
   • README.md   — instructions

5. To preview your site:
   • Go to Tools & Editors → HTTP Server
   • Open your browser to http://localhost:8080

EDITING YOUR SITE
─────────────────
• Go to My Projects → select your site
• Choose 'Edit files'
• The editor (nano-style) will open
• Make changes, then save

DEPLOYING YOUR SITE
───────────────────
• Copy files to a USB drive or
• Upload to a web host (GitHub Pages, etc.)

════════════════════════════════════════
TUT
